// Switch statement
let day = 1;
switch (day) {
  case 0:
    console.log("A");
    break;
  case 1:
    console.log("B");
    break;
  case 2:
    console.log("c");
    break;
  case 3:
    console.log("D");
    break;
  case 4:
    console.log("E");
    break;
  default:
    console.log("Not Valid..");
    break;
}
